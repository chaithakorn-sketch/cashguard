const editFooter = (url?: string, id?: string) => ({ type:'box', layout:'vertical', paddingStart:'20px', paddingEnd:'20px', paddingBottom:'18px',
  contents:[ solidBtn('แก้ไขข้อมูล', url ? { uri:url } : { data:`action=view&id=${id}` }) ] });

