// money: 2 decimals + thousands sep. signMoney prefixes −/+ and colours.
const money = (n: number) => Number(n).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
const baht = (n: number) => money(n) + ' ฿';

