// Header = red+Cammo background IMAGE (baked, bleeds like the mockup) with the pill,
// eyebrow and title as REAL Flex text overlaid on top (crisp, correct sizes).
function headerBox(name: string) {
  const s = HEADER_SPEC[name] || HEADER_SPEC['ask-evidence'];
  const bg = { type:'image', url:`${AB}/headers/bg-${s.pose}.png`, size:'full', aspectRatio:'360:132', aspectMode:'cover' };
  const pill = { type:'box', layout:'horizontal', contents:[
    { type:'box', layout:'horizontal', flex:0, backgroundColor:'#FFFFFF', cornerRadius:'16px',
      paddingTop:'3px', paddingBottom:'3px', paddingStart:'6px', paddingEnd:'9px', spacing:'4px', alignItems:'center',
      contents:[
        { type:'image', url:LOGO, size:'14px', aspectMode:'fit', flex:0 },
        { type:'text', text:'Carcamstore', color:RED, weight:'bold', size:'xxs', gravity:'center', flex:0 },
      ]},
    { type:'filler' },
  ]};
  const overlay = { type:'box', layout:'vertical', position:'absolute',
    offsetTop:'0px', offsetStart:'0px', offsetEnd:'0px', offsetBottom:'0px',
    paddingTop:'12px', paddingBottom:'12px', paddingStart:'18px', paddingEnd:'18px',
    contents:[
      pill,
      { type:'filler' }, // pushes the eyebrow+title group to the bottom edge (reliable on LINE)
      { type:'box', layout:'vertical', spacing:'none', contents:[
        { type:'text', text:s.eyebrow, color:'#FFFFFFCC', size:'xs', weight:'bold', offsetTop:'7px' },
        { type:'text', text:s.title, color:'#FFFFFF', weight:'bold', size:'xxl', wrap:false },
      ]},
    ]};
  return { type:'box', layout:'vertical', paddingAll:'0px', contents:[ bg, overlay ] };
}

// ---- body primitives ----
const SEP = { type:'separator', margin:'16px', color:HAIR };
const captionRow = (t: string) => ({ type:'text', text:t, size:'xs', color:MUTE, weight:'bold', wrap:true });
// big amount, sign-coloured + two-tone (big integer, smaller decimal + ฿).
// kind: 'out' (−red) | 'in' (+green) | 'neutral' (ink, gray decimal)
function amount(value: number, kind: 'out' | 'in' | 'neutral') {
  const sign = kind === 'out' ? '−' : kind === 'in' ? '+' : '';
  const color = kind === 'out' ? RED : kind === 'in' ? GREEN : INK;
  const s = money(value); const dot = s.indexOf('.');
  const intPart = dot < 0 ? s : s.slice(0, dot);
  const dec = dot < 0 ? '' : s.slice(dot); // ".00"
  return { type:'text', margin:'xs', weight:'bold', wrap:false, contents:[
    { type:'span', text:`${sign}${intPart}`, size:'4xl', color, weight:'bold' },
    { type:'span', text:`${dec} ฿`, size:'lg', color: kind === 'neutral' ? GRAY : color, weight:'bold' },
  ]};
}
function kv(label: string, value: string, strong = true) {
  return { type:'box', layout:'horizontal', margin:'md', contents:[
    { type:'text', text:label, size:'sm', color:GRAY, flex:0, gravity:'center' },
    { type:'text', text:value, size:'sm', color:strong?VAL:GRAY, weight:strong?'bold':'regular', align:'end', gravity:'center', wrap:true },
  ]};
}
function balanceRow(label: string, value: string | number, color = INK) {
  return { type:'box', layout:'horizontal', margin:'lg', alignItems:'center',
    contents:[
      { type:'text', text:label, size:'sm', color:GRAY, flex:1, gravity:'center' },
      { type:'text', text:baht(Number(value)), size:'xl', weight:'bold', color, align:'end', gravity:'center' },
    ]};
}
